public class PieceSimple {
    int base;
    int hauteur;

    public PieceSimple(int base, int hauteur) {
        this.base = base;
        this.hauteur = hauteur;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getHauteur() {
        return hauteur;
    }

    public void setHauteur(int hauteur) {
        this.hauteur = hauteur;
    }
}
